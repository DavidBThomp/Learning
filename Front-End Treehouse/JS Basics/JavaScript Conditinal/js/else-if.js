const weather = 'snow';

if ( weather === 'sun' ) {
    console.log("It is sunny so I am going swimming!");
} else if ( weather === 'rain' ) {
    console.log("It is raining so I will read a book.");
} else if ( weather === 'snow' ) {
    console.log("It is snowing so I am going sledding!");
} else {
    console.log("I don't know what I'm doing today");
}

// The previous must be false for next to be executed. If next is false it will continue down list.